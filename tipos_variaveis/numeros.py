# Inteiro
numero_inteiro = 4
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

#type()
print("tipo da variavel inteiro = ", type(numero_inteiro))
print("Tipo da variavel real = ", type(numero_real))

# soma +
soma = 1 + 1
print("1 + 1 = ", soma)

#subtração -
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

# multiplicacao
multiplicacao = 2 * 2
print("2 x 2 =", multiplicacao)

#divisao /
divisao = 5 / 2
print("5 / 2 ", divisao)
print("Tipo da variavel do resuldtado da divisao = ", type(divisao))

#int ()
print("valor em inteiro = ", int(divisao))

#flaot()
print("Valor em float = ", float(soma))

#Modulo %
modulo = 5 % 2 
print("Modulo = ", modulo)

#divisao inteiro //
